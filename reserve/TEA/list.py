original_list = [0x9b, 0xaa, 0x47, 0xb0, 0x32, 0xc5, 0x9d, 0xe9, 0x3f, 0x36, 0x3d, 0x63, 0xc3, 0x80, 0x5c, 0x8f, 0x87, 0x51, 0x87, 0xee, 0x77, 0x64, 0x90, 0xb1, 0x6b, 0x41, 0x6d, 0xfd, 0xcd, 0x1c, 0x5e, 0x3a]

# 将原始列表转换为四个字节一个元素的形式
new_list = [original_list[i] | (original_list[i + 1] << 8) | (original_list[i + 2] << 16) | (original_list[i + 3] << 24) for i in range(0, len(original_list), 4)]

# 以十六进制形式输出
hex_output = [hex(val) for val in new_list]

print(hex_output)
