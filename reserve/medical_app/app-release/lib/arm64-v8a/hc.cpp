#include<bits/stdc++.h>

using namespace std;

int flag[36]={0x56,0x4,0xb0,0xd4,0x9c,0x63,0x4d,0x30,0x96,0xce,0xc0,0x5,0x93,0xbe,0x3b,0x82,0x52,0x4b,0x16,0xb2,0x8a,0x33,0xb7,0x4d,0x6d,0x7b,0x99,0x50,0xc2,0xb1,0xc,0x12,0xe1,0x84,0xa,0x93};
int key[16]=
{   
	0x01, 0x00, 0x00, 0x00, 0x10, 0x00, 0x00, 0x00, 0x00, 0x01, 
	0x00, 0x00, 0x00, 0x10, 0x00, 0x00
};
int KeyStream[36],num;
int S[256]; 
char T[256];    

int main()
{
	int i=0,j=0;
    for(i=0;i<256;i++)
	{
        S[i]=i;
        T[i]=static_cast<char>(key[i&0xF]);
	}
	for(i=0;i<256;i++)
	{
		j=(j+S[i]+T[i])%256;
		swap(S[i],S[j]);
	}
	i=0,j=0;
    for(int k=0;k<36;k++)
	{
        i=i+1;
        j=(j+S[i])%256;
        swap(S[i],S[j]);
        KeyStream[num++]=S[(S[i]+S[j])%256];
    }
    for(int k=0;k<36;k++)
    {
		printf("%d\n",KeyStream[k]);
		flag[k]^=KeyStream[k];
	}
	return 0;
}
